function validatePhone(p) {
  var phoneRe = /^(\d{1})(\-|\s)?((\(\d{3}\))|\d{3})(\-|\s)?(\d{3})(\-|\s)?(\d{4})$/;
  var digits = p.replace(/\D/g, "");
